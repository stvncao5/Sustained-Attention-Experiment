function effectSize = cohenD(nullValues, actualValues)
    
    % Calculate the pooled standard deviation.
    pooled_standard_deviation = ...
        ( var(nullValues) + var(actualValues) ) ...
                                 /                              ...
                                 2                              ;

    % Calculate the difference between the null mean and the group mean.
    difference_of_means = ...
        ( mean(nullValues) - mean(actualValues) );
    
    % Get the effect size.
    cohen_d = ...
        difference_of_means ...
                /           ...
        pooled_standard_deviation;
    
    effectSize = cohen_d;
    
end